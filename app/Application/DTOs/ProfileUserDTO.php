<?php

namespace App\Application\DTOs;

class ProfileUserDTO
{
    /**
     * Create a new class instance.
     */
    public string $name;
    public string $email;

    public function __construct(string $name, string $email)
    {
        $this->name = $name;
        $this->email = $email;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getEmail(): string
    {
        return $this->email;
    }
}
